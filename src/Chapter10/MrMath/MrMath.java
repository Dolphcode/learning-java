package Chapter10.MrMath;

// Behold static imports, these are the things that save us a lot of typing
import static java.lang.Math.*;
import static java.lang.System.out;

public class MrMath {
	
	public static void main (String[] args) {
		out.println("Hi! I'm Mr. Math, saving this programmer some typing");
		out.println("Absolute Value! " + abs(-5));
		out.println("Square Roots! " + sqrt(25));
	}
	
}
